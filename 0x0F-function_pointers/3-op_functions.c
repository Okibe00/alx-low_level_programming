#include "3-calc.h"
/**
  * op_add - add number
  * @a: num1
  * @b: num2
  * description: adds two numbers and returns result
  * Return: sum of numbers
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - subtract number
  * @a: num1
  * @b: num2
  * description: subtract two numbers and returns result
  * Return: result
*/

int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - multiply two number
  * @a: num1
  * @b: num2
  * description: multiply two numbers and returns result
  * Return: result
*/

int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - divide number
  * @a: num1
  * @b: num2
  * description: divides two numbers and returns result
  * Return: result
*/

int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - mods number
  * @a: num1
  * @b: num2
  * description: modulo of two numbers and returns result
  * Return: sum of numbers
*/

int op_mod(int a, int b)
{
	return (a % b);
}

